// TL-14290 duedate in dp_plan_program_assign must not be -1, instead use 0.
function totara_plan_upgrade_fix_invalid_program_duedates() {
    global $DB;

    $sql = "UPDATE {dp_plan_program_assign} SET duedate = 0 WHERE duedate = -1";
    $DB->execute($sql);
}
